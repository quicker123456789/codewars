function isValidWalk(walk) {
  var dir = {};
  walk.forEach(elem=> dir[elem] = dir[elem]+1 || 1);  
  return (walk.length > 10 || walk.length < 10) ? false :
    dir['n']==dir['s'] && dir['w']==dir['e'];
}
